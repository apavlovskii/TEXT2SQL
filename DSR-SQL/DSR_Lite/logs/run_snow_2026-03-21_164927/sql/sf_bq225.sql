SELECT COUNT(*) AS language_count, CASE
    WHEN c."sample_path" ILIKE '%.asm' OR c."sample_path" ILIKE '%.nasm' THEN 'Assembly'
    WHEN c."sample_path" ILIKE '%.c' OR c."sample_path" ILIKE '%.h' THEN 'C'
    WHEN c."sample_path" ILIKE '%.cs' THEN 'C#'
    WHEN c."sample_path" ILIKE '%.c++' OR c."sample_path" ILIKE '%.cpp' OR c."sample_path" ILIKE '%.h++' OR c."sample_path" ILIKE '%.hpp' THEN 'C++'
    WHEN c."sample_path" ILIKE '%.css' THEN 'CSS'
    WHEN c."sample_path" ILIKE '%.clj' THEN 'Clojure'
    WHEN c."sample_path" ILIKE '%.lisp' THEN 'Common Lisp'
    WHEN c."sample_path" ILIKE '%.d' THEN 'D'
    WHEN c."sample_path" ILIKE '%.dart' THEN 'Dart'
    WHEN c."sample_path" ILIKE 'Dockerfile' OR c."sample_path" ILIKE '%.dockerfile' THEN 'Dockerfile'
    WHEN c."sample_path" ILIKE '%.ex' OR c."sample_path" ILIKE '%.exs' THEN 'Elixir'
    WHEN c."sample_path" ILIKE '%.erl' THEN 'Erlang'
    WHEN c."sample_path" ILIKE '%.go' THEN 'Go'
    WHEN c."sample_path" ILIKE '%.groovy' THEN 'Groovy'
    WHEN c."sample_path" ILIKE '%.html' OR c."sample_path" ILIKE '%.htm' THEN 'HTML'
    WHEN c."sample_path" ILIKE '%.hs' THEN 'Haskell'
    WHEN c."sample_path" ILIKE '%.hx' THEN 'Haxe'
    WHEN c."sample_path" ILIKE '%.json' THEN 'JSON'
    WHEN c."sample_path" ILIKE '%.java' THEN 'Java'
    WHEN c."sample_path" ILIKE '%.js' OR c."sample_path" ILIKE '%.cjs' THEN 'JavaScript'
    WHEN c."sample_path" ILIKE '%.jl' THEN 'Julia'
    WHEN c."sample_path" ILIKE '%.kt' OR c."sample_path" ILIKE '%.ktm' OR c."sample_path" ILIKE '%.kts' THEN 'Kotlin'
    WHEN c."sample_path" ILIKE '%.lua' THEN 'Lua'
    WHEN c."sample_path" ILIKE '%.matlab' OR c."sample_path" ILIKE '%.m' THEN 'MATLAB'
    WHEN c."sample_path" ILIKE '%.md' OR c."sample_path" ILIKE '%.markdown' OR c."sample_path" ILIKE '%.mdown' THEN 'Markdown'
    WHEN c."sample_path" ILIKE '%.php' THEN 'PHP'
    WHEN c."sample_path" ILIKE '%.ps1' OR c."sample_path" ILIKE '%.psd1' OR c."sample_path" ILIKE '%.psm1' THEN 'PowerShell'
    WHEN c."sample_path" ILIKE '%.py' THEN 'Python'
    WHEN c."sample_path" ILIKE '%.r' THEN 'R'
    WHEN c."sample_path" ILIKE '%.rb' THEN 'Ruby'
    WHEN c."sample_path" ILIKE '%.rs' THEN 'Rust'
    WHEN c."sample_path" ILIKE '%.scss' THEN 'SCSS'
    WHEN c."sample_path" ILIKE '%.sql' THEN 'SQL'
    WHEN c."sample_path" ILIKE '%.sass' THEN 'Sass'
    WHEN c."sample_path" ILIKE '%.scala' THEN 'Scala'
    WHEN c."sample_path" ILIKE '%.sh' OR c."sample_path" ILIKE '%.bash' THEN 'Shell'
    WHEN c."sample_path" ILIKE '%.swift' THEN 'Swift'
    WHEN c."sample_path" ILIKE '%.ts' THEN 'TypeScript'
    WHEN c."sample_path" ILIKE '%.vue' THEN 'Vue'
    WHEN c."sample_path" ILIKE '%.xml' THEN 'XML'
    WHEN c."sample_path" ILIKE '%.yml' OR c."sample_path" ILIKE '%.yaml' THEN 'YAML'
    ELSE 'Other'
END AS programming_language
FROM "SAMPLE_FILES" f
JOIN "SAMPLE_CONTENTS" c ON f."id" = c."id"
WHERE c."content" IS NOT NULL
GROUP BY programming_language
ORDER BY language_count DESC
LIMIT 10;
